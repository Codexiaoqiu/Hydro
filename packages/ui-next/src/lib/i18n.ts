export const catalogs: Record<Locale, Catalog> = {
  zh_CN: zhCN,
  // zh_TW currently falls back to en — translation TBD.
  zh_TW: en,
  en,
};

// DEV-only invariant: catches drift between the Locale union and the keys
// actually present in `catalogs`. Skipped in production bundles so that a
// stale build does not crash at runtime.
if (typeof import.meta !== 'undefined' && (import.meta as { env?: { DEV?: boolean } }).env?.DEV) {
  const _catalogKeys = Object.keys(catalogs) as Locale[];
  const _localeUnion: Locale[] = ['zh_CN', 'zh_TW', 'en'];
  const _missing = _localeUnion.filter((k) => !(k in catalogs));
  const _extra = _catalogKeys.filter((k) => !_localeUnion.includes(k));
  if (_missing.length || _extra.length) {
    console.warn(
      `[i18n] catalogs and Locale union out of sync: missing=${JSON.stringify(_missing)} extra=${JSON.stringify(_extra)}`,
    );
  }
}

/** Resolve a locale, defaulting to en. */
export function resolveLocale(value?: string | null): Locale {
  if (!value) return 'en';
  if (value in catalogs) return value as Locale;
  if (value.startsWith('zh')) return 'zh_CN';
  return 'en';
}

/** Formatted translation. Falls back to en catalog, then the key. */
export function translate(catalog: Catalog, key: string, args?: Record<string, unknown>): string {
  const val = catalog[key] ?? en[key] ?? key;
  if (!args) return val;
  return val.replace(/\{(\w+)\}/g, (_, k) => String(args[k] ?? `{${k}}`));
}

/** Resolve the runtime locale from window / navigator. SSR-safe. */
export function detectLocale(): Locale {
  if (typeof window === 'undefined') return 'en';
  const w = window as unknown as { __hydro_locale?: string };
  if (w.__hydro_locale) return resolveLocale(w.__hydro_locale);
  const nav = (typeof navigator !== 'undefined' ? (navigator.language || '') : '').toLowerCase();
  return resolveLocale(nav);
}

/** React hook: returns a stable `t(key, args?)` bound to the current locale. */
export function useTranslate() {
  const locale = useMemo(() => detectLocale(), []);
  return useMemo(() => {
    const catalog = catalogs[locale] ?? en;
    return (key: string, args?: Record<string, unknown>) => translate(catalog, key, args);
  }, [locale]);
}
